package Modelo;

public class Cliente {
	/**
	 * Creamos tres variable nombre apellidos y edad
	 */
	private String nombre;
	private String apellidos;
	private int edad;
	/**
	 * 
	 * @param nombre
	 * @param apellidos
	 * @param edad
	 */
	public Cliente(String nombre, String apellidos, int edad) {
		this.nombre = nombre;
		this.apellidos = apellidos;
		this.edad = edad;
	}
	
	
	
}
